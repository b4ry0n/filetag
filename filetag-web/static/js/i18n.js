/** Detect the preferred language, falling back through navigator.language → en. */
function _detectLang() {
    const stored = localStorage.getItem('filetag-lang');
    if (stored && TRANSLATIONS[stored]) return stored;
    const nav = (navigator.language || '').toLowerCase().slice(0, 2);
    return TRANSLATIONS[nav] ? nav : 'en';
}

/** The active language code (e.g. 'en', 'nl', 'de'). */
let _lang = _detectLang();

/**
 * Translate a key, substituting {placeholder} tokens from `params`.
 * Falls back to English if the key is missing in the active language.
 */
function t(key, params) {
    const dict = TRANSLATIONS[_lang] || TRANSLATIONS.en;
    const fallback = TRANSLATIONS.en;
    let str = Object.prototype.hasOwnProperty.call(dict, key) ? dict[key] : (fallback[key] || key);
    if (params) {
        for (const [k, v] of Object.entries(params)) {
            str = str.replaceAll('{' + k + '}', String(v));
        }
    }
    return str;
}

/** Switch the active language, persist the choice, and re-apply to the DOM. */
function setLang(code) {
    if (!TRANSLATIONS[code]) return;
    _lang = code;
    localStorage.setItem('filetag-lang', code);
    document.documentElement.lang = code;
    applyI18n();
}

/** Current active language code. */
function getLang() { return _lang; }

/**
 * Apply translations to all [data-i18n] elements in the document.
 *
 *   data-i18n="key"               → element.textContent = t(key)
 *   data-i18n-title="key"         → element.title = t(key)
 *   data-i18n-placeholder="key"   → element.placeholder = t(key)
 *   data-i18n-aria="key"          → element.setAttribute('aria-label', t(key))
 */
function applyI18n() {
    document.querySelectorAll('[data-i18n]').forEach(el => {
        el.textContent = t(el.dataset.i18n);
    });
    document.querySelectorAll('[data-i18n-title]').forEach(el => {
        el.title = t(el.dataset.i18nTitle);
    });
    document.querySelectorAll('[data-i18n-placeholder]').forEach(el => {
        el.placeholder = t(el.dataset.i18nPlaceholder);
    });
    document.querySelectorAll('[data-i18n-aria]').forEach(el => {
        el.setAttribute('aria-label', t(el.dataset.i18nAria));
    });
    // Update the language selector if it exists.
    const sel = document.getElementById('lang-select');
    if (sel) sel.value = _lang;
}

